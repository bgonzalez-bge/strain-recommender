/**
 * Returns a randomized title text for a given group type and name.
 * The title is generated by selecting a random prefix from a predefined list
 * (based on the group type) and concatenating it with the provided name.
 *
 * @function getRandomTitle
 * @param {string} type - The group type (e.g., "conditions", "symptoms", "effects", "strain").
 * @param {string} name - The name or value associated with the group (e.g., "Hybrid", "Anxiety").
 * @return {string} A formatted title string combining a random prefix and the name.
 *
 * @example
 * // Example usage:
 * getRandomTitle("strain", "Hybrid");
 * // → "If you want to try a Hybrid"
 */
export function getRandomTitle(type: string, name: string): string {
  let titles: string[] = [];

  switch (type) {
  case "conditions": {
    const phrasing = ConditionSymptomPhrasingMap[name.trim().toLowerCase()];
    if (phrasing) {
      const options = [phrasing.option1, phrasing.option2];
      return options[Math.floor(Math.random() * 2)];
    }
    titles = ConditionDashboardTitles;
    break;
  }
  case "symptoms": {
    const phrasing = ConditionSymptomPhrasingMap[name.trim().toLowerCase()];
    if (phrasing) {
      const options = [phrasing.option1, phrasing.option2];
      return options[Math.floor(Math.random() * 2)];
    }
    titles = SymptomDashboardTitles;
    break;
  }
  case "effects": {
    const phrasing = EffectPhrasingMap[name.trim().toLowerCase()];
    if (phrasing) {
      const options = [phrasing.option1, phrasing.option2];
      return options[Math.floor(Math.random() * 2)];
    }

    titles = EffectsDashboardTitles;
    break;
  }
  case "strain":
    // Indica uses special “an” wording
    titles = name.toLowerCase().startsWith("i") ?
      StrainTypeDashboardIndicaTitles :
      StrainTypeDashboardTitles;
    break;
  default:
    titles = ["Related to "];
  }

  const prefix = titles[Math.floor(Math.random() * titles.length)];
  return `${prefix}${name}`;
}

